    private void lstAutoresValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstAutoresValueChanged
        // TODO add your handling code here:
        Autor autor = (Autor)lstAutores.getSelectedValue();
        if(autor == null) return;
        txtCodAutor.setText(autor.getCodigoAutor());
        txtNomeAutor.setText(autor.getNomeAutor());
        txtNacAutor.setText(autor.getNacionalidadeAutor());
    }//GEN-LAST:event_lstAutoresValueChanged

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        lstAutores.setListData(new AutorDao().getAutores().toArray());
    }//GEN-LAST:event_formWindowActivated

    private void btnGravarAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarAutorActionPerformed
        // TODO add your handling code here:
        Autor autor = new Autor(txtCodAutor.getText(), txtNomeAutor.getText(), txtNacAutor.getText());
        if(autor.getCodigoAutor().isEmpty() || autor.getNomeAutor().isEmpty() || autor.getNacionalidadeAutor().isEmpty()) {
            JOptionPane.showMessageDialog(null, "VocÃª deve preencher todos os campos antes de gravar");
            return;
        }
        new AutorDao().gravar(autor);
        lstAutores.setListData(new AutorDao().getAutores().toArray());
        txtCodAutor.setText("");
        txtNomeAutor.setText("");
        txtNacAutor.setText("");
        txtNomeAutor.requestFocus();
    }//GEN-LAST:event_btnGravarAutorActionPerformed

